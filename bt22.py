char = str(input("Nhập vào 1 ký tự: "))

if char == 'A' or char == 'a':
    # Thực hiện các lệnh cho ký tự 'A' hoặc 'a'
    print(f"Đây là nguyên âm {char}")
elif char == 'E' or char == 'e':
    # Thực hiện các lệnh cho ký tự 'E' hoặc 'e'
    print(f"Đây là nguyên âm {char}.")
elif char == 'I' or char == 'i':
    # Thực hiện các lệnh cho ký tự 'I' hoặc 'i'
    print(f"Đây là nguyên âm {char}.")
elif char == 'O' or char == 'o':
    # Thực hiện các lệnh cho ký tự 'O' hoặc 'o'
    print(f"Đây là nguyên âm {char}.")
elif char == 'U' or char == 'u':
    # Thực hiện các lệnh cho ký tự 'U' hoặc 'u'
    print(f"Đây là nguyên âm {char}.")
else:
    # Nếu ký tự không phải là nguyên âm
    print(f"Ký tự {char} không phải là nguyên âm.")
